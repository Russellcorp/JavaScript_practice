// Program to print "Fizz" for multiples of 3 and "Buzz" for multiples of 5 (FizzBuzz)

// Loop from 1 to 30

// Print "Fizz" for multiples of 3, "Buzz" for multiples of 5, and "FizzBuzz" for multiples of both

let i = 1;

while(i <= 30) {
    if(i % 3 === 0 && i % 5 === 0) {
        console.log('FizzBuzz');
    } else if(i % 3 === 0) {
        console.log('Fizz');
    } else if (i % 5 === 0) {
        console.log('Buzz');
    } else {
        console.log(i);
    }
    i++;
}
